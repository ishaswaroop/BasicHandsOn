package com.globallogic.helloworld;
//write a program to give the color code and print the color name  //switch 
public class ColorCodeS {
 public static void main(String args[]) {
	 int code=1;
	 switch(code) {
	 case 1:System.out.println("red");
	 break;
	 case 2:System.out.println("blue");
	 break;
	 case 3:System.out.println("green");
	 break;
	 case 4:System.out.println("black");
	 break;
	 case 5:System.out.println("white");
	 break;
	 case 6:System.out.println("brown");
	 break;
	 case 7:System.out.println("pink");
	 break;
	 default:System.out.println("WRONG CHOICE");
	 }
 }
}
